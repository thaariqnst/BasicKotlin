package com.thaariq.basickotlin.controlflow

fun main() {
    var dzikir = 20
    while (dzikir >= 1){    //WHILE ADALAH LOOP ITU SENDIRI
        println("Subhanallah")
        dzikir--             //JIKA KONDISI BELUM FALSE, WHILE TIDAK AKAN BERHENTI
    }
var takbir = 0
    do {
        println("allahuakbar")
        takbir++
    }while (takbir <=-1)

}